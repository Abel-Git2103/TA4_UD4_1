
public class OperadoresAritmeticosAPP {

	public static void main(String[] args) {
		
		int variableA = 20, variableB = 20;
		System.out.println("La suma es igual a " + (variableA + variableB));
		System.out.println("La resta es igual a " + (variableA - variableB));
		System.out.println("La multiplicación es igual a " + (variableA * variableB));
		System.out.println("La division es igual a " + (variableA / variableB));
		System.out.println("El modulo es igual a " + (variableA % variableB));
	}
}
